static uint32_t bkr_rgb_size = sizeof(bkr_rgb);
static uint32_t stats_size   = sizeof(bkr_color_stats);

uint32_t _bkr_diff_square(bkr_rgb* a, bkr_rgb* b)
{
    return (a->red - b->red)     * (a->red - b->red) + 
           (a->green - b->green) * (a->green - b->green) +
           (a->blue - b->blue)   * (a->blue - b->blue);
}

int32_t _bkr_find_mindiff(bkr_rgb* a, _bkr_palette_stats* palette, uint32_t palette_count)
{
    uint32_t min_diff    = MAX_INT;
    uint32_t temp_diff   = 0;
    int32_t min_diff_idx = -1;
    uint32_t i;
    for(i = 0; i < palette_count; i++)
    {
        temp_diff = _bkr_diff_square(a, palette[i].color);

        if(min_diff_idx == -1 || min_diff > temp_diff)
        {
            min_diff_idx = i;
            min_diff     = temp_diff;
        }
    }

    return min_diff_idx;
}

static inline int _bkr_mindiff_calc(bkr_rgb* pixels, uint32_t pixel_count, int16_t gray_offset, _bkr_palette_stats* colored,
        uint32_t colored_count, _bkr_palette_stats* grayed, uint32_t grayed_count, bkr_color_stats stats[])
{
    int32_t index;
    _bkr_palette_stats* current_palette;
    uint32_t i;
    for(i = 0; i < pixel_count; i++)
    {
        // get current palette and the neareast one
        // color.
        current_palette = (gray_offset < 0 || !BKR_IS_GRAY(pixels + i, gray_offset)) ? colored : grayed;
        index           = _bkr_find_mindiff(pixels + i, current_palette, current_palette == colored ? colored_count : grayed_count);
        current_palette[index].count++;
    }

    // get answer!
    int answer_count = 0;
    uint32_t _i;
    for(_i = 0; _i < colored_count; _i++)
    {
        if(!colored[_i].count) continue;
        memcpy(&stats[answer_count].color, (colored[_i].color), bkr_rgb_size);
        stats[answer_count].value   = BKR_RGB_TO_INT32(colored[_i].color->red, colored[_i].color->green, colored[_i].color->blue);
        stats[answer_count++].count = colored[_i].count;
    }
    uint32_t i1;
    for(i1 = 0; i1 < grayed_count; i1++)
    {
        if(!grayed[i1].count) continue;
        memcpy(&stats[answer_count].color, (grayed[i1].color), bkr_rgb_size);
        stats[answer_count].value   = BKR_RGB_TO_INT32(grayed[i1].color->red, grayed[i1].color->green, grayed[i1].color->blue);
        stats[answer_count++].count = grayed[i1].count;
    }

    // sort for answer
    qsort(stats, answer_count, stats_size, _stats_cmp);

    return answer_count;
}

uint8_t _bkr_generate_colored_and_gray_pelette(bkr_palette_array* palette, _bkr_palette_stats** p_colored, _bkr_palette_stats** p_grayed,
        uint32_t* colored_count, uint32_t* grayed_count, int16_t gray_offset)
{
    _bkr_palette_stats* colored;
    _bkr_palette_stats* grayed;

    *colored_count = *grayed_count = 0;

    colored        = (_bkr_palette_stats*)calloc(palette->count, sizeof(_bkr_palette_stats));
    grayed         = (_bkr_palette_stats*)calloc(palette->count, sizeof(_bkr_palette_stats));

    // can't alloc space any more
    if(!colored || !grayed)
    {
        SAFE_FREE(colored);
        SAFE_FREE(grayed);
        return 0;
    }

    *p_colored = colored;
    *p_grayed  = grayed;

    if(gray_offset < 0)
    {
        // if gray offset is unlimit, all
        // colors are to go to `color_palette`
        uint32_t i;
        for(i = 0; i < palette->count; i++)
        {
            colored[i].color = palette->colors + i;
            colored[i].count = 0;
        }
        *colored_count = palette->count;
        grayed_count   = 0;
    }
    else
    {
        // otherwise, separate `color_palette`
        // and `gray_palette`
        bkr_rgb* temp_palette = palette->colors;
        uint32_t i;
        for(i = 0; i < palette->count; i++)
        {
            if(temp_palette[i].blue == temp_palette[i].red &&
                    temp_palette[i].red == temp_palette[i].green)
            {
                // if it meet this condition, that means
                // this color is a gray color
                grayed[*grayed_count].color   = temp_palette + i;
                grayed[(*grayed_count)++].count = 0;
            }
            else
            {
                // otherwise, it's a color color
                colored[*colored_count].color   = temp_palette + i;
                colored[(*colored_count)++].count = 0;
            }
        }

        // if the colors are oll colored or grayed, we
        // consider the colors are all in colored palette
        // and gray offset is -1 (unlimited)
        if(!grayed_count || !colored_count)
        {
            gray_offset = -1;
            if(grayed_count)
            {
                memcpy(colored, grayed, sizeof(_bkr_palette_stats) * palette->count);
                memset(grayed, 0, sizeof(_bkr_palette_stats) * palette->count);

                colored_count = grayed_count;
                grayed_count  = 0;
            }
        }
    }

    return 1;
}

int bkr_mindiff_calculate_color_stats(bkr_rgb* pixels, uint32_t pixel_count, bkr_color_stats stats[], bkr_mindiff_parameter* param)
{
    bkr_palette_array* total_palette = 0;
    _bkr_palette_stats* colored      = 0;
    _bkr_palette_stats* grayed       = 0;

    uint32_t colored_count = 0;
    uint32_t grayed_count  = 0;
    total_palette          = param->palette ? param->palette : (bkr_palette_array*)(&_default_palette);

    if(!total_palette->count) return -1;

    if(!_bkr_generate_colored_and_gray_pelette(total_palette, &colored,
                &grayed, &colored_count, &grayed_count, param->gray_offset))
    {
        return -1;
    }

    // start finding~
    //   让我们荡起双桨
    //     小船儿推开波浪 ヽ(=^･ω･^=)丿
    int result = _bkr_mindiff_calc(pixels, pixel_count, param->gray_offset, colored, colored_count, grayed, grayed_count, stats);

    SAFE_FREE(colored);
    SAFE_FREE(grayed);

    return result;
}
